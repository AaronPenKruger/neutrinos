#!/usr/bin/env python3
"""
data_to_maps.py

Convert the IceCube 10-year public point-source event sample (2008–2018) into
multi-energy HEALPix counts maps suitable for map-based SBI and OOD tests.

This script is intentionally conservative:
- It makes *counts* maps (no exposure correction, no unfolding).
- It does *not* apply masking by default (Capel+2020 reproduction baseline).
- It streams CSVs in chunks to avoid high memory use.

Expected data-release layout (after extracting the IceCube zip):
<DATA_DIR>/
  icecube_10year_ps/
    events/
      IC40_exp.csv
      IC59_exp.csv
      IC79_exp.csv
      IC86_I_exp.csv
      IC86_II_exp.csv
      IC86_III_exp.csv
      IC86_IV_exp.csv
      IC86_V_exp.csv
      IC86_VI_exp.csv
      IC86_VII_exp.csv
    uptime/
      IC40_exp.csv
      ...
    irfs/
      ...

Primary columns used (as provided in the release):
- "RA[deg]"   : reconstructed right ascension (J2000) in degrees
- "Dec[deg]"  : reconstructed declination (J2000) in degrees
- "log10(E/GeV)" : reconstructed muon-track energy proxy (log10 GeV)

The output is an .npz with:
- counts_map : int64 array, shape (n_bins, n_pix)
- E_edges_GeV: float array, bin edges in GeV
- nside, nest
- seasons, per-season event counts, and (optional) livetimes from uptime files

References:
- IceCube 10-year public point-source data release (2008–2018):
  https://icecube.wisc.edu/data-releases/2021/01/all-sky-point-source-icecube-data-years-2008-2018/
- SkyLLH dataset definition shows canonical filenames and the expected subfolder structure:
  https://icecube.github.io/skyllh/master/html/tutorials/publicdata_ps.html
"""

